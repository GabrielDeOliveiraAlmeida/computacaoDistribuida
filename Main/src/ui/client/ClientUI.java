    private void sistemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sistemaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        DefaultTableModel modelx = (DefaultTableModel) tabx.getModel();
        DefaultTableModel model2 = (DefaultTableModel) tab2.getModel();
        int tam = Integer.valueOf(tamanho.getText());
        model2.setRowCount(0);
        modelx.setRowCount(0);
        if (tam < 2) {
            return;
        }
        num = tam;
        for (int i = 0; i < tam; i++) {
            model.addColumn("A" + i);
        }

        Object[] zeros = new Object[num];
        Arrays.fill(zeros, 0);
        for (int i = 0; i < tam; i++) {
            model.addRow(zeros);
            model2.addRow(new Object[]{0});
            modelx.addRow(new Object[]{0});
        }

        tab.setModel(model);
        tab2.setModel(model2);
        tabx.setModel(modelx);

    }//GEN-LAST:event_sistemaActionPerformed

