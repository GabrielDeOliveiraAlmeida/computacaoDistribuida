    public void showResposta(LinearSystems resposta) {
        destravar();
        double[] resp = resposta.getResult();
        for (int i = 0; i < resp.length; i++) {
            tabx.setValueAt((Object) resp[i], i, 0);
        }

        DefaultTableModel model = (DefaultTableModel) hist.getModel();
        model.addRow(new Object[]{MatrixtoString(resposta.getMatrix()), VectortoString(resposta.getVetor()), VectortoString(resposta.getResult())});
    }

    public String VectortoString(double[] vetor) {
        String str = "[";
        for (int i = 0; i < vetor.length; i++) {
            str+= String.format("%.2f", vetor[i]) + "|";
        }
        str += "]";
        return str;
    }

    public String MatrixtoString(double[][] matrix) {
        String str="";
        for (int i = 0; i < matrix.length; i++) {
            str += "[";
            for (int j = 0; j < matrix.length; j++) {
                str += String.format("%.2f", matrix[i][j]) + "|";
            }
            str += "]";
        }
        return str;
    }

    
